// Javascript modules
/*
  > Javascript modules merupakan fitur dimana kita bisa membuat module (kode javascript yang bisa digunakan ulang) untuk kode javascript lain
  > Javascript modules diawali pada teknologi bernama NodeJS, selanjutnya banyak library yang mengadospi penggunaan javascript modules di NodeJS agar bisa berjalan di web browser, seperti library CommonJS, AMD, RequireJS, dan lain lain
  > Namun untungnya, saat ini browser modern sudah banyak mendukung javascript modules, kecuali jika masih menggunakan internet Expoler
*/

// Import dan Export
/*
  > Secara garis besar, ada dua kata kunci yang digunakan dalam javascript modules, yaitu import dan export
  > Import merupakan kata kunci yang digunakan untuk mengambil sesuatu dari module lain
  > Export merupakan kata kunci yang digunakan untuk membari tahu module lain, bagian mana yang ingin kita expose keluar, sehingga bisa di import oleh module lain
*/
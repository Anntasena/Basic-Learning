// Fetch API
/*
  > Fetch API merupakan fitur baru sebagai alternatif AJAX
  > Saat menggunakan AJAX, kita perlu menggunakan callback untuk menerima response atau data dari server, sedangkan fetch api menggunakan promise, sehingga penggunaan fetch api menjadi lebih mudah dibanding AJAX
  > Hampir semua browser modern sekarang sudah mendukung menggunakan fetch API
  > Apa yang bisa kita lakukan di AJAX bisa dilakukan di fetch API
*/

// Request
/*
  > Saat kita mengirim request menggunakan fetch api, kita perlu membuat object request
  > Hal ini menjadi lebih rapih dibanding menggunakan AJAX
  > Kita bisa membuat object request, lalu dikirim menggunakan fetch api
  > Dan saat membuat request, kita bisa menentukan data yang ingin kita kirim
*/

// Response
/*
  > Hasil dari fetch API adalah sebuah promise
  > Promise tersebut berisikan object response
  > Ada banyak property dan method pada object response, kita bisa menggunakannya sesuai yang kita butuhkan
*/
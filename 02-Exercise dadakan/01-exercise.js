// kalkulator Body Mass Index (BMI)
const beratBadan = 52;
const tinggiBadan = 120;

const BMI = beratBadan / (tinggiBadan * tinggiBadan);
console.log(BMI);

// konversi suhu celcius ke farenheit
const celcius = 52;
const farentheit = (celcius * 9) / 5 + 32;
console.log(farentheit ,"farenheit");

// menghitung luas dan keliling lingkaran
const jariJari = 14;
const pi = 3.14;
const kelilingLingkaran = 2 * pi * jariJari;
const luasLingkaran = pi * jariJari ** 2;

console.log("keliling lingkaran",kelilingLingkaran);
console.log("luas lingkaran", luasLingkaran);


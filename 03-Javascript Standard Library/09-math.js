// Math
/*
  > Math merupakan class di javascript yang berisikan static property dan method untuk oprasi matematika
  > Ada banyak sekali static property dan method di math
  > Math hanya bisa digunakan untuk tipe data number, tidak bisa digunakan untuk tipe data BigInt
*/

// Kode: math
console.log(Math.max(10, 11, 9, 5, 12)); // 12
console.log(Math.min(10, 11, 9, 5, 12)); // 5
console.log(Math.round(10.5)); // 11
console.log(Math.round(10.4)); // 10
console.log(Math.floor(10.8)); // 10 => selalu di bulatkan kebawah
console.log(Math.ceil(10.8)); // 11 => selalu di bulatkan ke atas

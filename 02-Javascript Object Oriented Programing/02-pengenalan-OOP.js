// Pengenalan Object Oriented Programing

// Apa itu OOP?
/*
  > Object oriented programing adalah sudut pandang bahasa pemrograman yang berkonsep "objek"
  > Ada banyak sudut pandang bahasa pemrograman, namum OOP adalah yang sangat populer saat ini
  > Ada berberapa istilah yang perlu dimengerti dalam OOP, yaitu Object dan Class
*/

// Apa itu object?
// Object adalah data yang berisi field / properties / attributes dan method / function / behavior

// Apa itu class?
/*
  > Class adalah blueprint, prototype atau cetakan untuk membuat object
  > Class berisikan deklarasi semua properties dan fucntions yang dimiliki object
  > Setiap object selalu dibuat dari class
  > Dan sebuah class bisa membuat object tanpa batas
*/


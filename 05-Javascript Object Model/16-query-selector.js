// Query Selector
/*
  > Sebelumnya kita sudah menggunakan method querySelectorAll() pada document, sekarang kita akan membahas tentang query selector
  > Query selector merupakan method untuk menseleksi node sesuai dengan pola yang ingin kita cari
  > Terdapat dua method untuk query selector:
    - Document.querySelector(pola) digunakan untuk menyeleksi node pertama yang sesuai dengan pola
    - Document.querySelectorAll(pola) digunakan untuk menyeleksi semua node yang sesuai dengan pola
*/

// Pola Query Selector
/*
  > Pola untuk query selector adalah menggunakan CSS selector
  > Penggunaan polanya mirip ketika kita membuat selector di CSS
*/

// Universal Selector
/*
  > Universal selector merupakan selector untuk menyeleksi semua element
  > Kita bisa menggunakan karakter *
*/


// Variable
/*
  Variable (variabel) dalam pemrograman adalah seperti wadah yang digunakan untuk menyimpan data. Anda memberikan nama pada wadah ini, 
  dan Anda dapat menempatkan berbagai jenis data di dalamnya, seperti angka, teks, atau objek.
  
  > Variable adalah tempat untuk menimpan data
  > Dengan menyimpan data variable, kita menggunakannya lagi dengan menyebut nama variablenya
  > Javascript adalah dynamic language, artinya variable di javascript tidak terpaku harus menggunakan satu tipe data, kita bisa mengubah tipe data di variable yang sama
*/

var namaPertama = "Syahrin"; // var bisa di declare dan di deassisment
let NamaKedua = "Matlail"; // let tidak bisa di declare tetapi bisa di deassisment
const namaKetiga = "Fajri"; // const tidak bisa di delcare dan di deassisment

// biasanya programer javascript menggunakan format "camel case" 
// Camel case adalah salah satu konvensi penamaan (naming convention) dalam pemrograman yang digunakan untuk memberi nama variabel, fungsi, atau identifikasi lainnya. Dalam camel case, setiap kata dalam nama dimulai dengan huruf kecil, kecuali kata pertama, dan setiap kata selanjutnya diawali dengan huruf besar tanpa spasi di antara kata-kata tersebut.


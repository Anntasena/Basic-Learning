// Tipe Data String
/*
  > Tipe data string adalah tipe data untuk menyimpan teks atau urutan karakter, bentuknya bermacam macam seperti
    - kata kata
    - frasa
    - atau hanya satu karakter
*/
let nama = "Jhon"; // tanda kutip ganda
let kota = 'New York'; // tanda kutip tunggal
let huruf = `A`; // tanda backtic

// Escape Sequence
/*
  > Serangkaian karakter khusus yang digunakan dalam string untuk mengekspresikan karakter yang sulit atau tidak mungkin ditulis secara langsung dalam teks.
    - \n = ENTER
    - \t = TAB
    - \' = ' (kutip tunggal)
    - \" = " (kutip ganda)
    - \\ = / (backslash)
*/
console.log("Syahrin \nMatlail \nFajri")
console.log("Nama: \tSyahrin")
console.log("\"Syahrin\"")
console.log("\'Syahrin\'")
console.log("\\Syarhin\\")


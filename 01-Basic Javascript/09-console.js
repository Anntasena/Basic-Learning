// Console
/*
  > Javascript memiliki fitur untuk melakukan logging bernama "console"
  > Logging adalah mekanisme yang biasa dilakukan oleh programmer untuk menampilkan informasi dari aplikasi yang sedang berjalan, tanpa harus menggangu alur alur kerja aplikasi dan juga interaksi user
  > Untuk melakukan ini, kita menggunakan fitur Console di javascirpt
  > Untuk menggunakan console, kita cukup gunakan perintah console di kode javascript
*/

/*
  > console.info(...) / console.log(...) = memberitahu informasi
  > console.warn(...) = memberitahu informasi peringatan
  > console.error(...) = memberitahu informasi error
  > console.table(...) = memberitahu informasi dalam bentuk tabel
*/

console.log("Hello World!");
console.info("Hello World!"); // tidak berwarna
console.debug("Hello World!"); // bisanya tidak terlihat
console.warn("Hello World!"); // berwarna kuning
console.error("Hello World!"); // berwarna merah
// fact >> makin ke bawah main tinggi levelnya 
// Perbedaan di atas akan terlihat jika kita menggunakan browser dan masuk ke console browsernya